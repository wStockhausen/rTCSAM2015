#'
#'@title Plot model fits to abundance, biomass and size frequencies as z-scores for fishery data components.
#'
#'@param res - model results list object
#'
#'@export
#'
plotZScoresForFisheries<-function(res,showPlot=FALSE){
    ps.fsh<-list();
    fshs<-names(res$model.fits$fisheries)
    for (fsh in fshs){
        ps.ret<-list();
        fit<-res$model.fits$fisheries[[fsh]]$retained.catch;
        if (!is.null(fit)){
            if (!is.null(fit$abundance)){
                cat("Plotting retained catch abundance zscores for",fsh,"\n")
                afits<-fit$abundance$fits;
                ps.ret$abund<-plotZScoresGG(afits,ylab='abundance',label=paste(fsh,": ","retained catch abundance",sep=''),showPlot=showPlot);
            }
            if (!is.null(fit$biomass)){
                cat("Plotting retained catch biomass zscores for",fsh,"\n")
                afits<-fit$biomass$fits;
                ps.ret$biom<-plotZScoresGG(afits,ylab='biomass',label=paste(fsh,": ","retained catch biomass",sep=''),showPlot=showPlot);
            }
            if (!is.null(fit$n.at.z)){
                cat("Plotting retained catch size frequency zscores for",fsh,"\n")
                ps.ret$zfs<-plotZScoresGG.SizeFreqs(fit$n.at.z,res$mc,label=paste(fsh,": ","retained catch",sep=''))
                cat("Plotting ESSs for size frequencies.\n")
                ps.ret$effn<-plotEffNsGG(fit$n.at.z,res$mc,label=paste(fsh,": ","retained catch",sep=''),showPlot=showPlot)
            }
        }
        ps.dsc<-list();
        fit<-res$model.fits$fisheries[[fsh]]$discard.catch;
        if (!is.null(fit)){
            if (!is.null(fit$abundance)){
                cat("Plotting discard catch abundance zscores for",fsh,"\n")
                afits<-fit$abundance$fits;
                ps.dsc$abund<-plotZScoresGG(afits,ylab='abundance',label=paste(fsh,": ","discard catch abundance",sep=''),showPlot=showPlot);
            }
            if (!is.null(fit$biomass)){
                cat("PLotting discard catch biomass zscores for",fsh,"\n")
                afits<-fit$biomass$fits;
                ps.dsc$biom<-plotZScoresGG(afits,ylab='biomass',label=paste(fsh,": ","discard catch biomass",sep=''),showPlot=showPlot);
            }
            if (!is.null(fit$n.at.z)){
                cat("Plotting discard catch size frequency zscores for",fsh,"\n")
                ps.dsc$zfs<-plotZScoresGG.SizeFreqs(fit$n.at.z,res$mc,label=paste(fsh,": ","discard catch",sep=''),showPlot=showPlot)
                cat("Plotting ESSs for size frequencies.\n")
                ps.dsc$effn<-plotEffNsGG(fit$n.at.z,res$mc,label=paste(fsh,": ","discard catch",sep=''),showPlot=showPlot)
            }
        }
        ps.tot<-list();
        fit<-res$model.fits$fisheries[[fsh]]$total.catch;
        if (!is.null(fit)){
            if (!is.null(fit$abundance)){
                cat("Plotting total catch abundance zscores for",fsh,"\n")
                afits<-fit$abundance$fits;
                ps.tot$abund<-plotZScoresGG(afits,ylab='abundance',label=paste(fsh,": ","total catch abundance",sep=''),showPlot=showPlot);
            }
            if (!is.null(fit$biomass)){
                cat("Plotting total catch biomass zscores for",fsh,"\n")
                afits<-fit$biomass$fits;
                ps.tot$biom<-plotZScoresGG(afits,ylab='biomass',label=paste(fsh,": ","total catch biomass",sep=''),showPlot=showPlot);
            }
            if (!is.null(fit$n.at.z)){
                cat("Plotting total catch size frequencies for",fsh,"\n")
                ps.tot$zfs<-plotZScoresGG.SizeFreqs(fit$n.at.z,res$mc,label=paste(fsh,": ","total catch",sep=''),showPlot=showPlot)
                cat("Plotting ESSs for size frequencies.\n")
                ps.tot$effn<-plotEffNsGG(fit$n.at.z,res$mc,label=paste(fsh,": ","total catch",sep=''),showPlot=showPlot)
            }
        }
        ps.fsh[[fsh]]<-list(ret=ps.ret,dsc=ps.dsc,tot=ps.tot);
    }#fsh
    return(ps.fsh)
}
